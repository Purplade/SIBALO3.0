    <script>
        document.addEventListener('DOMContentLoaded', function() {
            var btn = document.getElementById('btn-toggle-sidebar');
            if (btn) {
                btn.addEventListener('click', function() {
                    document.body.classList.toggle('sidebar-collapsed');
                    // Ensure tabler layout recalculates width if needed
                    setTimeout(function() {
                        window.dispatchEvent(new Event('resize'));
                    }, 200);
                });
            }

            var editBtn = document.getElementById('btn-edit-current-admin');
            if (editBtn) {
                editBtn.addEventListener('click', function(e) {
                    e.preventDefault();
                    var token = '{{ csrf_token() }}';
                    var name = '{{ Auth::guard('user')->user()->name }}';
                    fetch('/admin/edit', {
                        method: 'POST',
                        headers: {
                            'Content-Type': 'application/x-www-form-urlencoded',
                            'X-CSRF-TOKEN': token,
                            'X-Requested-With': 'XMLHttpRequest'
                        },
                        body: 'name=' + encodeURIComponent(name)
                    })
                    .then(function(res) { return res.text(); })
                    .then(function(html) {
                        var container = document.getElementById('current-admin-editform');
                        if (container) {
                            container.innerHTML = html;
                        }
                        var modalEl = document.getElementById('modal-edit-current-admin');
                        if (modalEl && window.bootstrap && window.bootstrap.Modal) {
                            var modal = window.bootstrap.Modal.getOrCreateInstance(modalEl);
                            modal.show();
                        } else if (typeof $ !== 'undefined' && $('#modal-edit-current-admin').modal) {
                            $('#modal-edit-current-admin').modal('show');
                        }
                    })
                    .catch(function(err) {
                        alert('Gagal memuat form edit admin.');
                        console.error(err);
                    });
                });
            }
        });
    </script>
